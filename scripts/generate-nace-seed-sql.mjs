import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const sourceFile = path.join(repoRoot, "src", "utils", "naceDatabase.ts");
const outputFile = path.join(repoRoot, "supabase", "seed_nace_codes.sql");

const raw = fs.readFileSync(sourceFile, "utf8");

const itemRegex =
  /{\s*code:\s*"([^"]+)",\s*name:\s*"([^"]+)",\s*hazard_class:\s*"([^"]+)",\s*industry_sector:\s*"([^"]+)"\s*},/g;

const rows = [];
const seen = new Set();

let match;
while ((match = itemRegex.exec(raw)) !== null) {
  const [, nace_code, nace_title, hazard_class, sector] = match;
  if (seen.has(nace_code)) continue;
  seen.add(nace_code);

  rows.push({
    nace_code: nace_code.trim(),
    nace_title: nace_title.trim(),
    hazard_class: hazard_class.trim(),
    sector: sector.trim(),
  });
}

if (rows.length === 0) {
  throw new Error("No NACE rows extracted from naceDatabase.ts");
}

const escapeSql = (value) => value.replaceAll("'", "''");

const valuesSql = rows
  .map(
    (r) =>
      `('${escapeSql(r.nace_code)}','${escapeSql(r.nace_title)}','${escapeSql(r.hazard_class)}','${escapeSql(r.sector)}')`
  )
  .join(",\n");

const sql = `-- Generated by scripts/generate-nace-seed-sql.mjs
-- Inserts only missing NACE codes by nace_code

INSERT INTO public.nace_codes (nace_code, nace_title, hazard_class, sector)
VALUES
${valuesSql}
ON CONFLICT (nace_code) DO NOTHING;
`;

fs.writeFileSync(outputFile, sql, "utf8");
console.log(`Generated ${rows.length} rows -> ${outputFile}`);
