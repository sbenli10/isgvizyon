import fs from "node:fs";
import path from "node:path";
import process from "node:process";
import xlsx from "xlsx";

const repoRoot = process.cwd();
const sourceArg = process.argv[2];

if (!sourceArg) {
  console.error("Usage: node scripts/import-nace-from-xlsx.mjs <xlsx-file-path>");
  process.exit(1);
}

const sourceFile = path.resolve(sourceArg);
if (!fs.existsSync(sourceFile)) {
  console.error(`XLSX file not found: ${sourceFile}`);
  process.exit(1);
}

const naceDatabasePath = path.join(repoRoot, "src", "utils", "naceDatabase.ts");
const seedSqlPath = path.join(repoRoot, "supabase", "seed_nace_codes.sql");

const workbook = xlsx.readFile(sourceFile, { cellDates: false });
const firstSheetName = workbook.SheetNames[0];
if (!firstSheetName) {
  throw new Error("Excel dosyasında okunabilir bir sayfa bulunamadı.");
}

const sheet = workbook.Sheets[firstSheetName];
const rows = xlsx.utils.sheet_to_json(sheet, {
  defval: "",
  raw: false,
});

const CODE_HEADERS = ["NACE Rev.2 Kodu", "NACE Kodu", "NACE Code"];
const TITLE_HEADERS = ["NACE Rev.2 Tanımı", "NACE Tanımı", "Faaliyet Tanımı"];
const HAZARD_HEADERS = ["Tehlike Sınıfı", "Tehlike Sinifi"];

function getCell(row, headers) {
  for (const header of headers) {
    if (row[header] != null && String(row[header]).trim()) {
      return String(row[header]).trim();
    }
  }
  return "";
}

function escapeTs(value) {
  return value.replaceAll("\\", "\\\\").replaceAll('"', '\\"');
}

function escapeSql(value) {
  return value.replaceAll("'", "''");
}

function normalizeHazardClass(value) {
  const normalized = value
    .trim()
    .toLocaleLowerCase("tr-TR")
    .replaceAll("ı", "i");

  if (normalized.includes("cok tehlikeli")) return "Çok Tehlikeli";
  if (normalized.includes("tehlikeli") && !normalized.includes("az")) return "Tehlikeli";
  return "Az Tehlikeli";
}

const parsedRows = rows
  .map((row) => {
    const code = getCell(row, CODE_HEADERS);
    const name = getCell(row, TITLE_HEADERS);
    const hazard = normalizeHazardClass(getCell(row, HAZARD_HEADERS));

    return {
      code,
      name,
      hazard_class: hazard,
      industry_sector: name,
    };
  })
  .filter((row) => row.code && row.name);

const dedupedRows = Array.from(
  parsedRows.reduce((map, row) => {
    if (!map.has(row.code)) {
      map.set(row.code, row);
    }
    return map;
  }, new Map()).values(),
);

if (dedupedRows.length === 0) {
  throw new Error("Excel dosyasından geçerli NACE satırı okunamadı.");
}

const naceDatabaseContent = `// Auto-generated by scripts/import-nace-from-xlsx.mjs
export interface NACECode {
  code: string;
  name: string;
  hazard_class: "Az Tehlikeli" | "Tehlikeli" | "Çok Tehlikeli";
  industry_sector: string;
}

export const NACE_DATABASE: NACECode[] = [
${dedupedRows
  .map(
    (row) =>
      `  { code: "${escapeTs(row.code)}", name: "${escapeTs(row.name)}", hazard_class: "${row.hazard_class}", industry_sector: "${escapeTs(row.industry_sector)}" },`
  )
  .join("\n")}
];

export function searchNACE(query: string): NACECode[] {
  if (!query) return NACE_DATABASE;

  const normalizedQuery = query.toLocaleLowerCase("tr-TR").trim();

  return NACE_DATABASE.filter((nace) => {
    const lowerName = nace.name.toLocaleLowerCase("tr-TR");
    const lowerSector = nace.industry_sector.toLocaleLowerCase("tr-TR");
    return nace.code.includes(query) || lowerName.includes(normalizedQuery) || lowerSector.includes(normalizedQuery);
  });
}
`;

const seedSqlContent = `-- Generated by scripts/import-nace-from-xlsx.mjs
-- Source: ${sourceFile.replaceAll("\\", "/")}

TRUNCATE TABLE public.nace_codes RESTART IDENTITY;

INSERT INTO public.nace_codes (nace_code, nace_title, hazard_class, sector)
VALUES
${dedupedRows
  .map(
    (row) =>
      `('${escapeSql(row.code)}','${escapeSql(row.name)}','${escapeSql(row.hazard_class)}','${escapeSql(row.industry_sector)}')`
  )
  .join(",\n")}
;
`;

fs.writeFileSync(naceDatabasePath, naceDatabaseContent, "utf8");
fs.writeFileSync(seedSqlPath, seedSqlContent, "utf8");

console.log(`Imported ${dedupedRows.length} NACE rows from ${sourceFile}`);
console.log(`Updated: ${naceDatabasePath}`);
console.log(`Updated: ${seedSqlPath}`);
